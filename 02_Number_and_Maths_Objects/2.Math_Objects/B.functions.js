// ln() with base

    // Returns the log value of number with base 10
    console.log(Math.log10(100))            // --> 2


// Absolute value (Magnitude)

    // Returns the absolute value of the number (Magnitude) 
    console.log(Math.abs(-5))               // --> 5


// ceil function

    // Returns smallest integer greater than equal to the float
    console.log(Math.ceil(7.3))             // --> 8
    console.log(Math.ceil(7))               // --> 7
    console.log(Math.ceil(-5.3))            // --> -5

    
// floor function

    // Returns Greatest Integer smaller than equal to the float
    console.log(Math.floor(9.3))             // --> 9
    console.log(Math.floor(5))               // --> 5
    console.log(Math.floor(-5.3))            // --> -6


// Max value

    // Returns the Maximum value present in the entered values
    console.log(Math.max(10, 20, 52, 100))  // --> 100


// ** Random Pick

    // Pick the random value in the interval [0, 1)
    console.log(Math.random())