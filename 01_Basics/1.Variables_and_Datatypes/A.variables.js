// Variables


// Declaration of variable with const

let name = 'Raj';                               // Giving the initial value of variable
name = 'Gwen';                                  // Giving the final value of variable

console.log(name);                              // Returns the last entered value "Gwen"



// Declaration of variable using const

const age = 17;
// age = 18;                                    // Cant redeclare the value of variable using "const" call

console.log(age);



